import UIKit

/// There are some utility things.
open class Util {
    
    /**
     An easy way to get status bar height.
     
     - returns: Status bar height. Simple, isn't it?
     */
    class func statusBarHeight() -> CGFloat {
        return UIApplication.shared.statusBarFrame.height
    }
    
}

